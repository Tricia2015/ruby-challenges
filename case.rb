weather = 'sunny'

case weather
when 'rainy'
	puts "You better carry and Umbrella!"
when 'sunny'
	puts "Don't forget your SHADES!!!"
when 'foggy'
	puts "Look out for oncoming Traffic!"
when 'snowing'
	puts "Don't forget your snow booties and dress warm!"
when 'hailing'
	puts "Look out and duck for cover!"
when 'tornado'
	puts "Better get under shelter IMMEDIATLY!"
else
	puts "Hmm, well hell....I don't know what you should do!"
end
